// arrow function expression with the multiple parameters
let arrowFunc = (carName, model, architect) => {
    return `The car name is ${carName} with the model of ${model} and the architecture of ${architect}`;
};
// calling function with arguments
console.log(arrowFunc('Sportage', 2023, 'KIA'));
console.log(arrowFunc('Vessel', 2019, 'Toyota'));
