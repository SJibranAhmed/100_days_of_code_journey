// loop for iterating number 1 to 10 except 5 for this we using "continue" method  
for (let i = 1; i <= 10; i++) {
    if (i === 5) {
        //continue for skipping coming value without inhibition of loop 
        continue;
    }
    else {
        console.log(i);
    }
}
